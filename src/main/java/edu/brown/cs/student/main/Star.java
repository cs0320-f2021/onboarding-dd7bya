package edu.brown.cs.student.main;

public class Star {
  public float starID;
  public String properName;
  public float X;
  public float Y;
  public float Z;

  public Star(float starID, String properName, float X, float Y, float Z) {
    this.starID = starID;
    this.properName = properName;
    this.X = X;
    this.Y = Y;
    this.Z = Z;
  }
}
